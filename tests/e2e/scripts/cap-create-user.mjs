import { chromium } from '@playwright/test';
import fs from 'node:fs';
const browser = await chromium.launch();
const ctx = await browser.newContext({ viewport: { width: 1280, height: 900 } });
const p = await ctx.newPage();
await p.goto('http://127.0.0.1:8765/user/active/add', { waitUntil: 'networkidle' });
await p.waitForTimeout(500);
await p.screenshot({ path: 'tests/e2e/screenshots/final-pass/create-user-before.png', fullPage: true });
await browser.close();

const b2 = await chromium.launch();
const c2 = await b2.newContext({ viewport: { width: 1280, height: 900 } });
const p2 = await c2.newPage();
await p2.goto('https://demo.strichliste.org/#!/user', { waitUntil: 'networkidle' });
await p2.waitForTimeout(2000);
const fab = p2.locator('button').filter({ hasText: /^\+$/ }).first();
const cnt = await fab.count();
if (cnt) await fab.click();
await p2.waitForTimeout(1500);
await p2.screenshot({ path: 'tests/e2e/screenshots/final-pass/create-user-spa.png', fullPage: true });
fs.writeFileSync('tests/e2e/spa-snapshot/create-user.body.html', await p2.evaluate(() => document.body.outerHTML));
await b2.close();
console.log('done');
